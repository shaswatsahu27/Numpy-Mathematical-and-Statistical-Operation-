import numpy as np

# --- Task 2: Important Mathematical Operations ---

A = np.array([10, 20, 30, 40])
B = np.array([1, 2, 3, 4])

# 1. Addition (A + B)
print("Addition (A + B):", A + B)

# 2. Subtraction (A - B)
print("Subtraction (A - B):", A - B)

# 3. Multiplication (A * B)
print("Multiplication (A * B):", A * B)

# 4. Division (A / B)
print("Division (A / B):", A / B)

# 5. Power (A ** 2)
print("Power (A ** 2):", A ** 2)

print()

# --- Extra (Optional): Using NumPy functions ---
print("np.add(A, B):", np.add(A, B))
print("np.subtract(A, B):", np.subtract(A, B))
print("np.multiply(A, B):", np.multiply(A, B))
print("np.divide(A, B):", np.divide(A, B))
print("np.power(A, 2):", np.power(A, 2))